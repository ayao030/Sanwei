# ===================== 2. 定义通用绘制函数 =====================
def plot_2d_rods(
    coordinate_data,
    view_name,
    figsize=(12, 8),
    line_width=2,
    font_size=10,
    flip_x=False,
    flip_y=False,
):
    """
    绘制杆件的二维视图
    :param coordinate_data: 杆件坐标字典（key=杆件ID，value=两点坐标列表）
    :param view_name: 视图名称（用于标题）
    :param figsize: 画布尺寸
    :param line_width: 杆件线宽
    :param font_size: 标签字体大小
    :param flip_x: 是否对 X 轴镜像（左右颠倒）
    :param flip_y: 是否对 Y 轴镜像（上下颠倒）
    """
    # 创建画布
    fig, ax = plt.subplots(figsize=figsize)

    # 遍历绘制每个杆件
    for rod_id, points in coordinate_data.items():
        # 提取起点和终点坐标
        (x1, y1), (x2, y2) = points

        if flip_x:
            x1, x2 = -x1, -x2
        if flip_y:
            y1, y2 = -y1, -y2

        # 绘制杆件（直线）
        ax.plot([x1, x2], [y1, y2],
                linewidth=line_width,
                label=f'Rod {rod_id}',
                marker='o', markersize=4)  # 端点标记

        # 在杆件中点添加ID标签（避免重叠，可根据需要调整）
        mid_x = (x1 + x2) / 2
        mid_y = (y1 + y2) / 2
        ax.text(mid_x, mid_y, str(rod_id),
                fontsize=font_size - 2,
                ha='center', va='center',
                bbox=dict(boxstyle='round,pad=0.2', facecolor='white', alpha=0.8))

    # 设置图表样式
    ax.set_title(f'2D {view_name} View of Rods', fontsize=font_size + 2, fontweight='bold')
    ax.set_xlabel('X Coordinate', fontsize=font_size)
    ax.set_ylabel('Y Coordinate', fontsize=font_size)

    # 显示网格（便于核对坐标）
    ax.grid(True, linestyle='--', alpha=0.6)

    # 等比例显示（避免坐标拉伸）
    ax.set_aspect('equal', adjustable='box')

    # 图例（若杆件数量多，可注释掉，避免遮挡）
    # ax.legend(loc='best', fontsize=font_size-2)

    # 调整布局
    plt.tight_layout()

    # 显示图形
    plt.show()


